export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-6 py-2.5 rounded-full text-sm font-medium relative cursor-pointer transition-all duration-300 inline-block text-center focus:outline-none focus:ring-2 focus:ring-amber-400 focus:ring-offset-2 focus:ring-offset-stone-900";

  const variantStyles = {
    primary:
      "bg-amber-300/50 backdrop-blur-sm text-white hover:bg-amber-600 hover:shadow-lg hover:shadow-amber-900/20",
    secondary: "bg-transparent border border-amber-300/40 text-amber-100 backdrop-blur-sm hover:border-amber-300/60 hover:bg-amber-300/5",
    dark: "bg-stone-900/90 backdrop-blur-sm text-amber-100 border border-amber-800/30 hover:bg-stone-800/90 hover:border-amber-700/50",
    gradient:
      "bg-gradient-to-r from-amber-600 to-amber-700 text-white backdrop-blur-sm hover:from-amber-700 hover:to-amber-800 hover:shadow-lg hover:shadow-amber-900/20",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
